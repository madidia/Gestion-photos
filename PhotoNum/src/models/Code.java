package models;

public class Code {
	
	private int code;
	private int valeur;
	
	public Code(int code, int valeur) {
		super();
		this.code = code;
		this.valeur = valeur;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getValeur() {
		return valeur;
	}

	public void setValeur(int valeur) {
		this.valeur = valeur;
	}
	
	

}
